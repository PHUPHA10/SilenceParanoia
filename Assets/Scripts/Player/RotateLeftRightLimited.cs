using UnityEngine;

public class RotateLeftRightLimited : MonoBehaviour
{
    [Header("Mouse Settings")]
    public float sensitivity = 100f;

    [Header("Rotation Limits (degrees)")]
    [Tooltip("????????????????????????? (???? -40 ??? 40 ????)")]
    public float minAngle = -40f;   // ??????????? (??????????)
    public float maxAngle = 40f;    // ?????????? (??????????)

    [Header("Reference Direction")]
    [Tooltip("???????????????????????????????????? ???? ???, Pivot ?????; ????????????????????????????")]
    public Transform referenceDirection;

    private float centerYaw;   // ?????????? (?????? reference)
    private float deltaYaw;    // ???????????????? ????????

    void OnEnable()
    {
        // ?????????????????????????????
        Cursor.lockState = CursorLockMode.Locked;
        Cursor.visible = false;

        // ???????????? (center) ????????
        if (referenceDirection != null)
        {
            // ?????? Y ??? reference ???????????
            centerYaw = referenceDirection.eulerAngles.y;
        }
        else
        {
            // ?????????? reference ????????????????????????????????????
            centerYaw = transform.eulerAngles.y;
        }

        // ?????????????? 0 (????????????????)
        deltaYaw = 0f;

        ApplyRotation();
    }

    void Update()
    {
        float mouseX = Input.GetAxis("Mouse X") * sensitivity * Time.deltaTime;

        // ??????????????????????
        deltaYaw += mouseX;

        // ??????????????????????????????
        deltaYaw = Mathf.Clamp(deltaYaw, minAngle, maxAngle);

        ApplyRotation();
    }

    private void ApplyRotation()
    {
        float finalYaw = centerYaw + deltaYaw;
        // ?????????? Y ?????????? (??????????)
        transform.rotation = Quaternion.Euler(0f, finalYaw, 0f);
    }
}
