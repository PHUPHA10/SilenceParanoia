using UnityEngine;
using UnityEngine.UI;   // ?? ??????????? Outline / Image

public class ChatSwitcher : MonoBehaviour
{
    [Header("??????????")]
    public GameObject[] chatPanels;     // PhoneChat_Arm, PhoneChat_Mint, ...

    [Header("????????????????????")]
    public Outline[] profileOutlines;   // Outline ??? chatArm, chatMint, ...

    void Start()
    {
        OpenChat(0);  // ???????????????????????????
    }

    public void OpenChat(int index)
    {
        // ???/???????????????
        for (int i = 0; i < chatPanels.Length; i++)
        {
            if (chatPanels[i] != null)
                chatPanels[i].SetActive(i == index);
        }

        // ???/??????????????
        for (int i = 0; i < profileOutlines.Length; i++)
        {
            if (profileOutlines[i] != null)
                profileOutlines[i].enabled = (i == index);
        }
    }
}
